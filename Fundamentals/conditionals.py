#  Conditional Statements
temperature = 15    # Change value to see program run differently
if temperature > 30: # Always use a colon during if statements to indent
    print("It's warm")
    print("Drink water")
elif temperature > 20: # Else if statement
    print("It's nice")
# If neither is true
else:
    print("Brrr.")
    print("Maybe you should move!")
print("Done")


